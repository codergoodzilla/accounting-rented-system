package com.domain.location;

public class CoordinateHistory {

    private double lat;

    private double lng;

    private double accountingTiming;

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLng() {
        return lng;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }

    public double getAccountingTiming() {
        return accountingTiming;
    }

    public void setAccountingTiming(double accountingTiming) {
        this.accountingTiming = accountingTiming;
    }
}
